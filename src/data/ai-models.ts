// ─── Helper Functions ───────────────────────────────────────────────────────

/** Get model by ID */
export function getModelById(id: string): AIModel | undefined {
  return aiModels.find((m) => m.id === id);
}

/** Get models by family */
export function getModelsByFamily(family: string): AIModel[] {
  return aiModels.filter((m) => m.family === family);
}

/** Get models by best-for use case */
export function getModelsByBestFor(useCase: string): AIModel[] {
  return aiModels.filter((m) => m.bestFor.includes(useCase));
}

/** Get models that can run locally on consumer hardware */
export function getConsumerModels(): AIModel[] {
  return aiModels.filter(
    (m) => m.hardware.gpu === 'optional' || m.hardware.gpu === 'recommended'
  );
}

/** Get all unique model families */
export function getModelFamilies(): string[] {
  return [...new Set(aiModels.map((m) => m.family))].sort();
}

/** Get all unique licenses */
export function getModelLicenses(): string[] {
  return [...new Set(aiModels.map((m) => m.license.type))].sort();
}
